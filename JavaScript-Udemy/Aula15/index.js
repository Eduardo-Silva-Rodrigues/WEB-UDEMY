let num1 = 9.874854;
let num2 = Math.floor(num1); // Floor = Arredonda um número quebrado para baixo.

console.log(num2);

let num3 = 9.646874;
let num4 = Math.ceil(num3); // Ceil = Arredonda um número quebrado para cima.

console.log(num4);

let num5 = 9.50;
let num6 = Math.round(num5); // Round = Arredonda um número quebrado para o mais próximo.

console.log(num6);

console.log(Math.max(1,2,3,4,5,-9,12312.9)) // .max = Retorna o maior número.

console.log(Math.min(1,2,3,4,5,-9,12312.9)) // .min = Retorna o menor número.

console.log(Math.random() * (10 - 5) + 5); // .random = Retorna um número decimal aleatório.

console.log(Math.round(Math.random() * (10 - 5) + 5)); // .random = Retorna um número arredondado aleatório.

console.log(Math.pow(2, 10)); // .pow = Potenciação.

console.log(9 ** 0.5); // Raiz quadrada (1/2).

console.log(100 / 0); // Resultado = Infinity.